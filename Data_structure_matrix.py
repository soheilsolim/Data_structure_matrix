def find_zeros(matrix):
    """
    پیدا کردن تمام موقعیت‌های عدد ۰ در ماتریس
    ورودی: ماتریس به صورت لیست از لیست‌ها
    خروجی: لیست از تاپل‌های (row, col) که مقدارشان ۰ است
    """
    rows = len(matrix)
    if rows == 0:
        return []
    
    cols = len(matrix[0])
    zeros_positions = []
    
    for i in range(rows):          # پیمایش ردیف‌ها
        for j in range(cols):      # پیمایش ستون‌ها
            if matrix[i][j] == 0:  # اگر خانه صفر بود
                zeros_positions.append((i, j))  # موقعیت را اضافه کن
    
    return zeros_positions

# مثال ماتریس
matrix = [
    [1, 2, 0, 4],
    [5, 0, 7, 8],
    [9, 10, 11, 0],
    [0, 13, 14, 15]
]

# فراخوانی تابع
positions = find_zeros(matrix)

if positions:
    print("موقعیت‌های عدد ۰ در ماتریس:")
    for pos in positions:
        print(f"ردیف {pos[0]}, ستون {pos[1]}")
else:
    print("هیچ عدد ۰ در ماتریس وجود ندارد.")

